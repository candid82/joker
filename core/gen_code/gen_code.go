func (genEnv *GenEnv) emitVar(name string, obj interface{}) {
	if _, found := genEnv.Generated[name]; found {
		panic(fmt.Sprintf("already generated %s", name))
	}
	genEnv.Generated[name] = nil
	v := reflect.ValueOf(obj)
	*genEnv.Statics = append(*genEnv.Statics, fmt.Sprintf(`
var %s %s = %s`[1:],
		name, coreTypeName(v), genEnv.emitValue(v)))
	genEnv.Generated[name] = obj
}

func (genEnv *GenEnv) emitMembers(name string, obj interface{}) (members []string) {
	v := reflect.ValueOf(obj)
	kind := v.Kind()
	switch kind {
	case reflect.Map:
		if v.IsNil() {
			return
		}
		keys := v.MapKeys()
		for _, key := range keys {
			k := genEnv.emitValue(key)
			v := genEnv.emitValue(v.MapIndex(key))
			if v == "" {
				continue
			}
			members = append(members, fmt.Sprintf(`
	%s: %s,`[1:],
				k, v))
		}
	case reflect.Struct:
		vt := v.Type()
		numMembers := v.NumField()
		for i := 0; i < numMembers; i++ {
			vtf := vt.Field(i)
			val := genEnv.emitValue(v.Field(i))
			if val == "" {
				continue
			}
			members = append(members, fmt.Sprintf(`
	%s: %s,`[1:],
				vtf.Name, val))
		}
	default:
		panic(fmt.Sprintf("unsupported type %T for %s", obj, name))
	}
	return
}

func (genEnv *GenEnv) emitValue(v reflect.Value) string {
	v = UnsafeReflectValue(v)
	if v.IsZero() {
		return ""
	}

	switch pkg := path.Base(v.Type().PkgPath()); pkg {
	case "reflect":
		t := coreTypeString(fmt.Sprintf("%s", v))
		el := ""
		if t[0] != '*' {
			t = "*" + t
			el = ".Elem()"
		}
		return fmt.Sprintf("reflect.TypeOf((%s)(nil))%s", t, el) // TODO: Insert correct reflection info here
	case "core":
	case ".":
	default:
		panic(fmt.Sprintf("unexpected PkgPath `%s'", pkg))
	}

	switch v.Kind() {
	case reflect.Interface:
		if v.IsNil() {
			return ""
		}
		return genEnv.emitValue(v.Elem())

	case reflect.Ptr:
		return genEnv.emitPtrTo(v)

	case reflect.Bool:
		if v.Bool() {
			return "true"
		}
		return "false"

	case reflect.Int8, reflect.Int16, reflect.Int32, reflect.Int64, reflect.Int:
		return fmt.Sprintf("%d", v.Int())

	case reflect.Uint8, reflect.Uint16, reflect.Uint32, reflect.Uint64, reflect.Uint:
		return fmt.Sprintf("%d", v.Uint())

	case reflect.String:
		return strconv.Quote(v.String())

	case reflect.Slice, reflect.Array:
		return fmt.Sprintf(`%s{%s}`, coreTypeName(v), genEnv.emitSlice(v))

	case reflect.Struct:
		typeName := coreTypeName(v)
		obj := v.Interface()
		if obj == nil {
			return ""
		}
		name := uniqueId(obj)
		if _, yes := genEnv.Generated[name]; !yes {
			*genEnv.Statics = append(*genEnv.Statics, fmt.Sprintf(`
%s{%s}`[1:],
				typeName, joinMembers(genEnv.emitMembers(typeName, obj))))
			genEnv.Generated[name] = obj
		}
		return name

	case reflect.Map:
		typeName := coreTypeName(v)
		obj := v.Interface()
		if obj == nil {
			return ""
		}
		name := uniqueId(obj)
		if _, yes := genEnv.Generated[name]; !yes {
			*genEnv.Statics = append(*genEnv.Statics, fmt.Sprintf(`
%s{%s}`[1:],
				typeName, genEnv.emitMembers(typeName, obj)))
			genEnv.Generated[name] = obj
		}
		return name

	default:
		return fmt.Sprintf("nil /* UNKNOWN TYPE obj=%T v=%s v.Kind()=%s vt=%s */", v.Interface(), v, v.Kind(), v.Type())
	}
}

func (genEnv *GenEnv) emitPtrTo(ptr reflect.Value) string {
	if ptr.IsNil() {
		return "nil"
	}

	v := ptr.Elem()
	v = UnsafeReflectValue(v)

	if v.IsZero() {
		return ""
	}

	switch pkg := path.Base(v.Type().PkgPath()); pkg {
	case "core":
	case ".":
	default:
		panic(fmt.Sprintf("unexpected PkgPath `%s'", pkg))
	}

	switch v.Kind() {
	case reflect.Interface:
		if v.IsNil() {
			return "nil"
		}
		return genEnv.emitPtrTo(v.Elem())

	default:
		name, found := genEnv.Generated[v]
		if !found {
			obj := v.Interface()
			name := uniqueId(obj)
			genEnv.Generated[v] = nil
			genEnv.emitVar(name, obj)
			genEnv.Generated[v] = name
		}
		return "&" + name.(string)
	}
}

func (genEnv *GenEnv) emitSlice(v reflect.Value) string {
	numEntries := v.Len()
	el := []string{}
	for i := 0; i < numEntries; i++ {
		res := genEnv.emitValue(v.Index(i))
		if res == "" {
			el = append(el, "\tnil,")
		} else {
			el = append(el, "\t"+res+",")
		}
	}
	return joinMembers(el)
}

func coreTypeString(s string) string {
	return strings.Replace(s, "core.", "", 1)
}

func coreTypeName(v reflect.Value) string {
	return coreTypeString(v.Type().String())
}

func coreTypeOf(obj interface{}) string {
	return coreTypeName(reflect.ValueOf(obj))
}

func uniqueId(obj interface{}) string {
	switch obj := obj.(type) {
	case string:
		return "s_" + NameAsGo(obj)
	default:
	}
	return UniqueId(obj, nil)
}

func joinMembers(members []string) string {
	f := strings.Join(members, "\n")
	if f != "" {
		f = "\n" + f + "\n"
	}
	return f
}
